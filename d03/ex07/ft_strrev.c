char	*ft_strrev(char *str)
{
	int		a;
	int		b;
	char	t;

	a = 0;
	b = 0;
	while (str[b])
		b++;
	b--;
	while (a < b)
	{
		t = str[b];
		str[b] = str[a];
		str[a] = t;
		a++;
		b--;
	}
	return (str);
}
