int	ft_iterative_factorial(int nb)
{
	int i;

	if (nb < 0 || nb > 12)
		return (0);
	if (nb == 0)
		return (1);
	i = nb;
	while (i > 2)
	{
		i--;
		nb = nb * i;
	}
	return (nb);
}
